// writeProject emits the generated + support files for the app project.
// It never touches handlers.go except to guarantee it exists.
func writeProject(dir string, f *Form) error {
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(dir, "app.gen.go"), []byte(generateApp(f)), 0644); err != nil {
		return err
	}
	// The athutil/athui helper packages are machine-owned like app.gen.go: stamp
	// fresh copies every build so they stay in sync with the IDE. They are
	// internal packages of the generated module, imported as "<module>/athutil"
	// and "<module>/athui".
	if err := os.MkdirAll(filepath.Join(dir, "athutil"), 0755); err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(dir, "athutil", "athutil.go"), []byte(athutilSource), 0644); err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Join(dir, "athui"), 0755); err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(dir, "athui", "athui.go"), []byte(athuiSource), 0644); err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(dir, "go.mod"), []byte(generateGoMod()), 0644); err != nil {
		return err
	}
	// Makefile and README are user-facing conveniences: write them once, then
	// leave any edits alone on later builds.
	if err := writeIfMissing(filepath.Join(dir, "Makefile"), generateMakefile()); err != nil {
		return err
	}
	if err := writeIfMissing(filepath.Join(dir, "README.md"), generateReadme(f)); err != nil {
		return err
	}
	// Guarantee a stub exists for every wired signal so the generated code
	// always compiles. ensureHandlerStub is append-only: it never touches
	// handlers the user has already written.
	for _, w := range f.Widgets {
		for _, fn := range w.Signals {
			if _, err := ensureHandlerStub(dir, fn); err != nil {
				return err
			}
		}
	}
	if _, err := os.Stat(handlersPath(dir)); os.IsNotExist(err) {
		if err := os.WriteFile(handlersPath(dir), []byte(handlersHeader), 0644); err != nil {
			return err
		}
	}
	return nil
}

// buildApp resolves deps and compiles the project to dir/app. Returns combined
// tool output so the IDE can show it in its console.
func buildApp(dir string) (string, error) {
	var out strings.Builder
	steps := [][]string{
		{"go", "mod", "tidy"},
		{"go", "build", "-o", "app", "."},
	}
	for _, step := range steps {
		cmd := exec.Command(step[0], step[1:]...)
		cmd.Dir = dir
		cmd.Env = append(os.Environ(), "GOFLAGS=-mod=mod")
		o, err := cmd.CombinedOutput()
		out.Write(o)
		if err != nil {
			out.WriteString("\n" + strings.Join(step, " ") + " failed: " + err.Error() + "\n")
			return out.String(), err
		}
	}
	return out.String(), nil
}

// goModTidy resolves the project's dependencies (creating go.sum) so gopls and
// the compiler can find them. Best-effort; deps are already in the module cache.
func goModTidy(dir string) (string, error) {
	cmd := exec.Command("go", "mod", "tidy")
	cmd.Dir = dir
	cmd.Env = append(os.Environ(), "GOFLAGS=-mod=mod")
	out, err := cmd.CombinedOutput()
	return string(out), err
}

func runApp(dir string) error {
	cmd := exec.Command("./app")
	cmd.Dir = dir
	return cmd.Start()
}
